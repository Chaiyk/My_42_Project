#include <printf.h>

int	ft_putstr(char *c)
{
	int	i;

	i = 0;
	if (c)
	{
		while (c[i])
		{
			i += ft_putchar(c[i]);
		}
	}
	else
		i += ft_putstr("(null)");
	return (i);
}
