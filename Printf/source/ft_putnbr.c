#include <printf.h>

int	ft_putnbr(int n)
{
	int	i;

	i = 0;
	if (n == -2147483648)
	{
		ft_putstr("-2147483648");
		return (11);
	}
	if (n < 0)
	{
		i += ft_putchar('-');
		n = n * -1;
	}
	if (n >= 10)
	{
		i += ft_putnbr(n / 10);
		n %= 10;
	}
	if (n < 10)
		i += ft_putchar(n + 48);
	return (i);
}
